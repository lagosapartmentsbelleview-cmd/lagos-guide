function setReservationPolicyLang(lang) {
  const data = reservationPolicyTexts[lang] || reservationPolicyTexts.pt;
  const titleEl = document.getElementById("reservationPolicyTitle");
  const contentEl = document.getElementById("reservationPolicyContent");

  if (titleEl) titleEl.textContent = data.title;
  if (contentEl) contentEl.innerHTML = data.html;

  document
    .querySelectorAll(".legal-lang-btn")
    .forEach(btn => btn.classList.toggle("active", btn.dataset.lang === lang));
}

// ligar botões
document.querySelectorAll(".legal-lang-btn").forEach(btn => {
  btn.addEventListener("click", () => {
    setReservationPolicyLang(btn.dataset.lang);
  });
});

// idioma inicial (podes alinhar com currentLang se já o tiveres)
